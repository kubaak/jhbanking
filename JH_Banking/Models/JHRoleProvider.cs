        public override string[] GetRolesForUser(string username)
        {
            using (JH_DBContext db = new JH_DBContext())
            {
                int userId = Int32.Parse(username);
                if (db.UserAccount.Where(u => u.UserId == userId).FirstOrDefault().Admin == true)
                {
                    return new string[]{ "A" };
                }
                else
                {
                    return new string[] { "U" };
                }
            }
